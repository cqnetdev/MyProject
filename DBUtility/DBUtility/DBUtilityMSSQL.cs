        {
            using (SqlConnection connection = new SqlConnection(connectionString))
            {
                SqlCommand cmd = new SqlCommand(SQLString, connection);
                System.Data.SqlClient.SqlParameter myParameter = new System.Data.SqlClient.SqlParameter("@content", SqlDbType.NText);
                myParameter.Value = content;
                cmd.Parameters.Add(myParameter);
                try
                {
                    connection.Open();
                    cmd.CommandTimeout = 1800;
                    int rows = cmd.ExecuteNonQuery();
                    return rows;
                }
                catch (System.Data.SqlClient.SqlException e)
                {
                    throw e;
                }
                finally
                {
                    cmd.Dispose();
                    connection.Close();
                }
            }
        }

        /// <summary>
        /// 带事物的T-SQL,返回受影响的行数
        /// </summary>
        /// <param name="sql"></param>
        /// <param name="sp"></param>
        /// <returns></returns>
        public bool ExecuteTranNonQuery(string sql)
        {
            return ExecuteTranNonQuery(sql, null);
        }
        public bool ExecuteTranNonQuery(string sql, SqlParameter[] sp)
        {
            bool bo = true;
            using (SqlConnection conn = new SqlConnection(connectionString))
            {
                conn.Open();
                SqlTransaction stran = conn.BeginTransaction();
                try
                {
                    using (SqlCommand comm = new SqlCommand(sql, conn))
                    {
                        comm.CommandTimeout = 1800;
                        if (sp != null)
                        {
                            foreach (SqlParameter parameter in sp)
                            {
                                if ((parameter.Direction == ParameterDirection.InputOutput || parameter.Direction == ParameterDirection.Input) &&
                                    (parameter.Value == null))
                                {
                                    parameter.Value = DBNull.Value;
                                }
                                comm.Parameters.Add(parameter);
                            }
                        }
                        comm.Transaction = stran;
                        int i = comm.ExecuteNonQuery();
                        if (i < 0 || i == 0)
                        {
                            bo = false;
                        }
                    }
                    if (bo)
                    {
                        stran.Commit();
                    }
                    return bo;

                }
                catch (SqlException e)
                {
                    stran.Rollback();
                    throw e;
                }

            }
        }


